# 3. '0' or '1' digit acceptor

def IsSingleBinaryDigit(x):
    return x is 0 or x is 1  


print(IsSingleBinaryDigit(1)) # output: True
print(IsSingleBinaryDigit(5)) # output: False
print(IsSingleBinaryDigit(0)) # output: True


