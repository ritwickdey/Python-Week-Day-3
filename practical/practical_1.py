# 2. Create a ‘SayHello’ function that takes 
# two parameter – a greeting message & your name 
# and returns a string by concatenate your name 
# with the getting message. 


def SayHello(greeting, name):
    return greeting + ", " + name + "!"


print(SayHello("Hi", "Ritwick")) # Output : Hi, Ritwick!
print(SayHello("Hello", "Akash")) # Output : Hello, Akash!

